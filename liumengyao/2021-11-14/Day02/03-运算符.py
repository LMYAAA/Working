"""
运算符
    +   加
    -   减
    *   乘
    /   除
    %   取余
    //  取商
"""
print('-----运算符------')
print( 1 % 2 )
print('===' * 10)
print(['张三'] * 10)  # 列表中的元素乘10次
print(['张三', '李四'] * 10)  # 列表中的元素各乘10次



"""
关系运算
    <   
    <=
    >
    >=
    ==
    !=
    
    只返回两个值 : True False
"""
print('-----关系运算------')
print( 1 < 2 )      # True
print( 1 > 5 )      # False
print( 1 == '1' )   # False
print( 10 != '10' ) # True


